package com.gruppomcr.dao.model;

public class PermessiDAOModel {
	private int id;
	private String funzione;
	private int idProfilazione;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getFunzione() {
		return funzione;
	}
	public void setFunzione(String funzione) {
		this.funzione = funzione;
	}
	public int getIdProfilazione() {
		return idProfilazione;
	}
	public void setIdProfilazione(int idProfilazione) {
		this.idProfilazione = idProfilazione;
	}
	
	
}
